<script setup>
  import { ref, reactive } from  'vue'
  
  const counter = ref(0)
  const counterTitle = ref('My Counter')
  const counterData = reactive({
    count: 0,
    title: 'My Counter'
  })

  const increaseCounter = () => {
    counter.value++
    counterData.count++
  }
  const decreaseCounter = () => {
    counter.value--
    counterData.count--
  }
</script>

